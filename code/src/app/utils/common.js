export function isJSON(data) {
  try {
    if (typeof JSON.parse(data) === 'object') {
      return true
    }
  } catch (e) {
    // console.log(e)
    return false
  }
  return false
}

// 解析数组字段类型的数据为数组
export function parseTagsDataToArray(data) {
  if (!data) {
    return []
  }
  if (Array.isArray(data)) {
    return data
  }
  if (isJSON(data)) {
    return JSON.parse(data)
  }
  return []
}

