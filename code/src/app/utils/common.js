// 导出下载文件
export function download(url) {
  // 使用iframe导出
  let iframe = document.getElementById('ticketListDownLoadIframe')
  if (!iframe) {
    iframe = document.createElement('iframe')
    iframe.setAttribute('id', 'ticketListDownLoadIframe')
    iframe.setAttribute('width', '0')
    iframe.setAttribute('height', '0')
    document.body.appendChild(iframe)
  }
  iframe.setAttribute('src', url)
}

// 将参数和 window.LOWCODE_APP_KEY，以或的形式结合判断
export function orLowcode(...params) {
  return [...params, window.LOWCODE_APP_KEY].some(Boolean)
}

export function getAllFields(formLayoutVos) {
  let fields = []
  _.forEach(formLayoutVos, (d) => {
    if (d.type === 'group' || d.type === 'panel') {
      fields = [...d.fieldList, ...fields]
    } else {
      _.forEach(d.tabs, (tab) => {
        fields = [...fields, ...tab.fieldList]
      })
    }
  })
  return fields
}
